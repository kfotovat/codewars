#include <iostream>

using namespace std;

int main()
{
  int a;
  char c;
  float f;

  cout << "Hello from C++!" << endl;
  cout << "This int takes " << sizeof(a) << " bytes" << endl;
  cout << "This char takes " << sizeof(c) << " bytes" << endl;
  cout << "This float takes " << sizeof(f) << " bytes" << endl;
}
